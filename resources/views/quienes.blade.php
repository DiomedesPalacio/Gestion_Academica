            <h1 class="main__title">Historia de nuestra universidad</h1><br><br>           
           
               <p> La Universidad Nacional Abierta y a Distancia, (UNAD) es un Proyecto Educativo que nació con el nombre de Unidad Universitaria del Sur de Bogotá, UNISUR durante el gobierno de Belisario Betancur. <br/><br/>
                Surgió, mediante la Ley 52 de 1981, como un establecimiento público del orden nacional adscrito al Ministerio de Educación Nacional y transformada por el Congreso de la República mediante la Ley 396 del 5 de agosto de 1997 en la Universidad Nacional Abierta y a Distancia UNAD. <br/><br/>
                Se creó con el objeto de diseñar e implementar programas académicos con la estrategia pedagógica de la educación a distancia, que fuesen pertinentes con las necesidades locales, regionales, nacionales e internacionales y acordes con los retos y las demandas de una sociedad democrática, participativa y dinámica afines con modelos científicos, sociales y culturales que contextualizan al siglo XXI. <br/><br/>
                Desde su puesta en marcha – abril de 1982 -, la Universidad se ha caracterizado por su compromiso con las comunidades y poblaciones que no han tenido acceso a una capacitación técnica, socio humanística y comunitaria. También, por su contribución a la recuperación de los tejidos sociales, la generación de espacios laborales y la formación para la participación ciudadana. <br/><br/>
               
                <h2>Los acontecimientos más importantes</h2><br/><br/>

                <h3>2005</h3><br/>
                Mediante la Resolución 6215 del Ministerio de Educación Nacional, la UNAD fue reconocida legalmente con el carácter académico de Universidad, previo cumplimento de las condiciones establecidas normalmente. <br/><br/>
                <h3>2006</h3><br/>
                Mediante decreto 2770 de 2006, la UNAD se reconoce como ente autónomo de carácter nacional, con personería jurídica, autonomía académica, administrativa y financiera, y patrimonio independiente, adscrita al Ministerio de Educación Nacional. <br/><br/>
                <h3>2007</h3><br/>
                Se realizó el ejercicio de planeación para el diseño del plan de desarrollo 2007 – 2011, "por la calidad educativa y la equidad social".<br/><br/>

                <h3>2009 - 2012</h3><br/>
                La Universidad recibe sus certificados de calidad en la Norma Técnica Colombiana en Gestión Pública NTCGP 1000 y a la Norma Técnica en Calidad NTC ISO 9001. <br/>

                Así mismo, mediante la resolución No. 64329 del 14 de diciembre, la División de Signos Distintivos de la Superintendencia de la Industria y Comercio le concedió a la UNAD el depósito de la enseña comercial (logo UNAD).

                <h3>2012</h3><br/>
                Mediante Resolución 3988 del 18 de abril de 2012, UNAD recibe Alta Acreditación por parte del Ministerio de Educación Nacional para cinco programas: Comunicación Social, Licenciatura en Etnoeducación, Zootecnia, Ingeniería de Sistemas de la Escuela y Administración de Empresas. <br/><br/>

                <h3>20013 - 2014</h3><br/>
                La UNAD inaugura seis Centros Comunitarios de Atención Virtual en Pereira, Sahagún, Corozal, Pamplona, Quibdó y Neiva. <br/><br/>

                <h3>2014</h3><br/>
                La UNAD fortalece su infraestructura tecnológica con alcance para diversos dispositivos móviles. <br/>
                Así mismo, la UNAD es reconocida con un índice de transparencia del 88,5%, consolidándose como la segunda universidad pública más transparente del país. <br/><br/>

                <h3>2015</h3><br/>
                Por cuarto año consecutivo la UNAD se ubica en el primer puesto del indicador de madurez MECI con un 99,7% entre los entes universitarios autónomos de Colombia. <br/><br/>

                <h3>2016</h3><br/>
                Se mantiene el sello de calidad que expide el Departamento Administrativo de la Función Pública. <br/>
                Se inicia el proyecto CampoUNAD, Sembrando un país en red para la paz, como una estrategia  de movilidad formativa y productiva para la población rural. <br/><br/>

                <h3>2017</h3><br/>
                Nace "Arando la Educación" un proyecto de atención educativa a excombatientes y a población aledaña en zonas veredales transitorias. <br/>

                La UNAD obtiene la certificación internacional de calidad en sus programas de bilingüismo, de acuerdo a los lineamientos del Marco Común Europeo de Referencia. <br/><br/>

                <h3>2018</h3><br/>
                La UNAD alcanza la cifra de 215 Semilleros de Investigación y 45 Grupos de Investigación categorizados por Colciencias. <br/>

                La UNAD se posiciona entre las 3 empresas más innovadoras del Estado Colombiano, de acuerdo al Ministerio de Tecnologías de la Información y las Comunicaciones. <br/>

                La UNAD asume la presidencia de la Asociación Iberoamericana de Educación Superior a Distancia AIESAD. <br/><br/>

                <h3>2019</h3><br/>
                La UNAD fue certificada por Great Place to Work como una de las organizaciones con mejor ambiente laboral, superando a la mayoría de las organizaciones de Colombia que se han propuesto tener un ambiente laboral excelente. <br/><br/>

                <h3>2020</h3><br/>
                SGS Colombia certificó a la UNAD como una institución que respalda su oferta educativa con el desarrollo tecnológico de software especializado en cuanto al cumplimiento de los requisitos de ISO 9001:2015. <br/><br/></p>

         
